    public class FormSegitiga extends JFrame {
        public FormSegitiga() {
            setTitle("Form Segitiga");
            setSize(300, 200);
            setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            setLocationRelativeTo(null);

            JLabel labelBase = new JLabel("Alas:");
            JLabel labelHeight = new JLabel("Tinggi:");
            JTextField textBase = new JTextField(5);
            JTextField textHeight = new JTextField(5);
            JButton buttonCalculate = new JButton("Hitung Luas");
            JLabel labelResult = new JLabel("Hasil:");

            buttonCalculate.addActionListener(new ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    try {
                        double base = Double.parseDouble(textBase.getText());
                        double height = Double.parseDouble(textHeight.getText());
                        double area = 0.5 * base * height;
                        labelResult.setText("Hasil: " + area);
                    } catch (NumberFormatException ex) {
                        JOptionPane.showMessageDialog(null, "Masukkan angka yang valid");
                    }
                }
            });

            setLayout(new java.awt.FlowLayout());
            add(labelBase);
            add(textBase);
            add(labelHeight);
            add(textHeight);
            add(buttonCalculate);
            add(labelResult);
        }
    }

