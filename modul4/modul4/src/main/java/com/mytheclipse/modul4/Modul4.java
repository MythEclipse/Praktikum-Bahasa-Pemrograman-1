    public class FormLingkaranRuang extends JFrame {
        public FormLingkaranRuang() {
            setTitle("Form Lingkaran (Bangun Ruang)");
            setSize(300, 200);
            setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            setLocationRelativeTo(null);

            JLabel labelRadius = new JLabel("Jari-jari:");
            JTextField textRadius = new JTextField(5);
            JButton buttonCalculate = new JButton("Hitung Volume");
            JLabel labelResult = new JLabel("Hasil:");

            buttonCalculate.addActionListener(new ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    try {
                        double radius = Double.parseDouble(textRadius.getText());
                        double volume = (4.0 / 3.0) * Math.PI * Math.pow(radius, 3);
                        labelResult.setText("Hasil: " + volume);
                    } catch (NumberFormatException ex) {
                        JOptionPane.showMessageDialog(null, "Masukkan angka yang valid");
                    }
                }
            });

            setLayout(new java.awt.FlowLayout());
            add(labelRadius);
            add(textRadius);
            add(buttonCalculate);
            add(labelResult);
        }
    }
